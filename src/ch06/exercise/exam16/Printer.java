package ch06.exercise.exam16;

public class Printer {
	void println (int param1) {
		System.out.println(param1);
	}
	
	void println (boolean param1) {
		System.out.println(param1);
	}
	
	void println (double param1) {
		System.out.println(param1);
	}
	
	void println (String param1) {
		System.out.println(param1);
	}
}
